#!/usr/bin/env node
/**
 * vibe init --for-new-member — generate codebase onboarding guide
 *
 * Usage:
 *   node scripts/vibe-init-for-new-member.js [--workspace /path]
 *
 * Generates .vibe/onboarding.md with:
 * - Project structure overview
 * - Key files and their purpose
 * - Getting started steps
 * - Architecture notes (from package.json, README, .vibe/rules.md)
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const args = process.argv.slice(2);
const WORKSPACE = args.find(a => a.startsWith('--workspace='))?.split('=')[1]
	|| (args.includes('--workspace') ? args[args.indexOf('--workspace') + 1] : null)
	|| process.cwd();

function getDirectoryTree(dir, depth = 0, maxDepth = 2) {
	if (depth > maxDepth) return '';
	try {
		const entries = fs.readdirSync(dir, { withFileTypes: true })
			.filter(e => !['node_modules', '.git', 'dist', 'build', 'out', '.vibe'].includes(e.name))
			.slice(0, 20);
		return entries.map(e => {
			const indent = '  '.repeat(depth);
			if (e.isDirectory() && depth < maxDepth) {
				const children = getDirectoryTree(path.join(dir, e.name), depth + 1, maxDepth);
				return `${indent}📁 ${e.name}/\n${children}`;
			}
			return `${indent}📄 ${e.name}`;
		}).join('\n');
	} catch { return ''; }
}

function getGitStats() {
	try {
		const contributors = execSync('git shortlog -sn --no-merges | head -5', { encoding: 'utf-8', cwd: WORKSPACE }).trim();
		const firstCommit = execSync('git log --reverse --format="%ad" --date=short | head -1', { encoding: 'utf-8', cwd: WORKSPACE }).trim();
		const totalCommits = execSync('git rev-list --count HEAD', { encoding: 'utf-8', cwd: WORKSPACE }).trim();
		return { contributors, firstCommit, totalCommits };
	} catch { return null; }
}

function readFile(filePath, maxLines = 30) {
	if (!fs.existsSync(filePath)) return null;
	const lines = fs.readFileSync(filePath, 'utf-8').split('\n').slice(0, maxLines);
	return lines.join('\n');
}

// Main
console.log('📚 Generating codebase onboarding guide...\n');

const pkg = (() => {
	try { return JSON.parse(fs.readFileSync(path.join(WORKSPACE, 'package.json'), 'utf-8')); }
	catch { return {}; }
})();

const readme = readFile(path.join(WORKSPACE, 'README.md'), 20);
const rules = readFile(path.join(WORKSPACE, '.vibe', 'rules.md'), 30);
const gitStats = getGitStats();
const tree = getDirectoryTree(WORKSPACE);

const onboarding = `# Codebase Onboarding Guide

> Auto-generated by \`vibe init --for-new-member\` on ${new Date().toISOString().split('T')[0]}

---

## Project Overview

**Name:** ${pkg.name || path.basename(WORKSPACE)}  
**Version:** ${pkg.version || 'unknown'}  
**Description:** ${pkg.description || '(see README)'}

${gitStats ? `**Started:** ${gitStats.firstCommit} | **Commits:** ${gitStats.totalCommits}  
**Top contributors:**\n\`\`\`\n${gitStats.contributors}\n\`\`\`` : ''}

---

## Project Structure

\`\`\`
${tree}
\`\`\`

---

## Getting Started

${pkg.scripts ? `### Available Scripts

\`\`\`bash
${Object.entries(pkg.scripts).slice(0, 10).map(([k, v]) => `npm run ${k.padEnd(20)} # ${v.slice(0, 60)}`).join('\n')}
\`\`\`` : ''}

### First Steps

1. Clone the repository
2. Run \`npm install\` (or \`yarn\` / \`pnpm install\`)
3. ${pkg.scripts?.dev ? `Start dev server: \`npm run dev\`` : pkg.scripts?.start ? `Start: \`npm start\`` : 'Follow README for setup'}
4. Open in VibeIDE — the \`.vibe/\` config is already set up

---

## AI Agent Rules

${rules ? `From \`.vibe/rules.md\`:\n\n${rules}` : 'No AI rules configured. Add to \`.vibe/rules.md\`.'}

---

## Key Files

| File | Purpose |
|------|---------|
| \`package.json\` | Project dependencies and scripts |
| \`.vibe/rules.md\` | AI agent instructions |
| \`.vibe/constraints.json\` | Protected files/patterns |
${pkg.main ? `| \`${pkg.main}\` | Main entry point |\n` : ''}${readme ? '| `README.md` | Project documentation |' : ''}

---

${readme ? `## From README\n\n${readme}\n\n---\n` : ''}

*This guide was auto-generated. For questions, check \`.vibe/rules.md\` or ask in team chat.*
`;

const outputPath = path.join(WORKSPACE, '.vibe', 'onboarding.md');
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, onboarding);
console.log(`✅ Generated: .vibe/onboarding.md`);
console.log(`\n📖 Share this with new team members or open in VibeIDE.`);
